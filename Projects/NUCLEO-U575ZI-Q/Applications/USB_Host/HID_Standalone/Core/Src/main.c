/**
  * @brief  CPU L1-Cache enable.
  * @param  None
  * @retval None
  */
static void CACHE_Enable(void)
{
  /* Configure ICACHE associativity mode */
  HAL_ICACHE_ConfigAssociativityMode(ICACHE_1WAY);

  /* Enable I-Cache */
  HAL_ICACHE_Enable();
}

